export class SkillCompendium {
    
    /*****************************************************************************
     * SkillCompendium constructor
     * 
     * @method
     *****************************************************************************/
    constructor() {
        this.skills = skillLinks
    }

    /*****************************************************************************
      * Returns the skill with the corresponding ID
      * 
      * @method
      * @param {int} skillID
      * @returns {Skill} 
      *****************************************************************************/
     getSkill(skillID) {
        return this.skills[skillID]
    }
}